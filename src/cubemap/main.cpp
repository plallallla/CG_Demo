class CubeWidhet : public GLWidget
{
    ShaderProgram _cube_shader{"../glsl/skybox/cubemaps.vs", "../glsl/skybox/cubemaps.fs"};
    ShaderProgram _normal_reflect_shader{"../glsl/skybox/env.vs", "../glsl/skybox/env_reflect.fs"};
    ShaderProgram _normal_s_shader{"../glsl/skybox/cubemaps.vs", "../glsl/skybox/cubemaps.fs"};
    ShaderProgram _sky_shader{"../glsl/skybox/skybox.vs", "../glsl/skybox/skybox.fs"};
    GLuint _wood_texture;
    GLuint _sky_texture;
    VertexArray _cube_va;
    VertexArray _cube_normal_va;
    VertexArray _sky_va;
    virtual void application() override
    {
        _wood_texture = TEXTURE_MANAGER.load_texture("../resources/textures/container2.png", TEXTURE_2D_RGBA);
        _sky_texture = TEXTURE_MANAGER.load_cube_texture
        ({
            "../resources/textures/skybox/right.jpg",
            "../resources/textures/skybox/left.jpg",
            "../resources/textures/skybox/top.jpg",
            "../resources/textures/skybox/bottom.jpg",
            "../resources/textures/skybox/front.jpg",
            "../resources/textures/skybox/back.jpg",
        });
        _cube_normal_va.attach_vertex_buffer(PN_LAYOUT, BUFFER.generate_buffer(GL_ARRAY_BUFFER, sizeof(cubeVerticesNormal), cubeVerticesNormal));
        _cube_va.attach_vertex_buffer(PT_LAYOUT, BUFFER.generate_buffer(GL_ARRAY_BUFFER, sizeof(cubeVertices), cubeVertices));
        _sky_va.attach_vertex_buffer(P_LAYOUT, BUFFER.generate_buffer(GL_ARRAY_BUFFER, sizeof(skyboxVertices), skyboxVertices));
        _cube_shader.set_sampler(0, "texture1");
        _sky_shader.set_sampler(0, "skybox");
        _normal_reflect_shader.set_sampler(0, "skybox");
    }

    void render_cube()
    {
        _cube_shader.use();
        _cube_shader.set_uniform("model", glm::mat4(1.0));
        _cube_shader.set_uniform("view", CAMERA.get_view_matrix());
        _cube_shader.set_uniform("projection", get_projection());
        _cube_shader.active_sampler(0, _wood_texture);
        _cube_va.bind();
        glDrawArrays(GL_TRIANGLES, 0, 36);
    }

    void render_reflect()
    {
        _normal_reflect_shader.use();
        _normal_reflect_shader.set_uniform("model", glm::mat4(1.0));
        _normal_reflect_shader.set_uniform("view", CAMERA.get_view_matrix());
        _normal_reflect_shader.set_uniform("projection", get_projection());
        _normal_reflect_shader.set_uniform("cameraPos", CAMERA.get_position());
        _normal_reflect_shader.active_sampler(0, _wood_texture);
        _cube_normal_va.bind();
        glDrawArrays(GL_TRIANGLES, 0, 36);
    }

    void render_box()
    {
        _sky_shader.use();
        _sky_shader.set_uniform("view", glm::mat4(glm::mat3(CAMERA.get_view_matrix())));//移除位移
        _sky_shader.set_uniform("projection", get_projection());
        _sky_shader.active_sampler(0, _sky_texture, GL_TEXTURE_CUBE_MAP);
        _sky_va.bind();
        glDrawArrays(GL_TRIANGLES, 0, 36);
    }

    virtual void render_loop() override
    {
        glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
        glEnable(GL_DEPTH_TEST);
        render_reflect();
        // render_cube();
        glDepthFunc(GL_LEQUAL);
        render_box();
        glDepthFunc(GL_LESS);
    }

public:
    CubeWidhet(int width, int height, std::string_view title) : GLWidget(width,height,title) 
    {

    }
};

int main()
{
    CubeWidhet c{800, 600, "1111"};
    c.render();
    return 0;
}